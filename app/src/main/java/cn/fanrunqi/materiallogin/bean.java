package cn.fanrunqi.materiallogin;

public class bean {


    String title;

    public bean(String title, int image) {
        this.title = title;
        this.image = image;
    }

    int image;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }






}
